/*
 * 文字列リテラルのコピー
 *
 * この例では、Rustでの文字列リテラルの扱いを示しています。
 * 文字列リテラル（&str型）はスタック上に固定サイズで格納され、
 * 複数の変数に同じ文字列リテラルを代入しても、参照のコピーが行われるだけです。
 * 
 * 文字列リテラルはコンパイル時にバイナリに埋め込まれ、不変（イミュータブル）です。
 * このような単純なコピーは所有権の移動を伴わないため、両方の変数を後で使用できます。
 */

fn main() {
    let s1 = "Hello!";
    let s2 = s1;
    println!("s1={}, s2={}", s1, s2);
}
