/*
 * 文字列挿入
 *
 * この例では、Rustの文字列（String型）に対する挿入操作と文字の操作を示しています。
 * insert_str()メソッドで文字列を、insert()メソッドで単一の文字を挿入できます。
 * また、remove()メソッドで特定の位置の文字を削除できます。
 *
 * 文字列操作の特徴：
 * 1. insert_str(位置, 文字列) - 指定位置に文字列を挿入
 * 2. insert(位置, 文字) - 指定位置に1文字を挿入
 * 3. remove(位置) - 指定位置の文字を削除
 * 4. これらの操作には可変（mut）な文字列が必要
 * 5. 位置はバイトインデックスで指定する（マルチバイト文字に注意）
 *
 * この例では「Hello,World!」という文字列に対して、挿入と削除の操作を行っています。
 */

fn main() {
    let mut s1 = String::from("Hello,World!");
    s1.insert_str(6, " Rust ");
    s1.insert(7, '*');
    s1.insert(12, '*');
    s1.remove(5);
    println!("{}", s1);
}
