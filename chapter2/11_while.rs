/*
 * while文による繰り返し
 *
 * この例では、Rustのwhile文を使用した条件付きループの基本的な使い方を示しています。
 * while文は、指定された条件が真（true）である間、繰り返し処理を実行します。
 *
 * while文の特徴：
 * 1. 条件式が真である限り処理を継続する
 * 2. 条件式が偽になると処理を終了する
 * 3. 条件を最初にチェックするため、条件が最初から偽の場合は一度も実行されない
 *
 * この例では、1から指定された最大値までの合計を計算するために
 * while文を使用しています。
 */

fn main() {
    let max: i32 = 100;
    let mut sum: i32 = 0;
    let mut count: i32 = 1;

    // countがmaxより大きくなるまで繰り返す
    while count <= max {
        sum += count;
        count += 1;
    }

    println!("1から{}までの合計は、{} です。", max, sum);
}
