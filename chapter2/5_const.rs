/*
 * 定数の使用
 *
 * この例では、Rustの定数（const）の基本的な使い方を示しています。
 * 定数は値が変更されないことを保証する変数で、コンパイル時に値が決定されます。
 *
 * 定数の特徴：
 * 1. constキーワードを使用して宣言する
 * 2. 型注釈が必須である（例：const X: i32 = 10;）
 * 3. 命名規則として通常は大文字とアンダースコアを使用する（SCREAMING_SNAKE_CASE）
 * 4. プログラムのどこからでもアクセス可能（スコープ内であれば）
 * 5. コンパイル時に値が確定している必要がある
 *
 * この例では、二つの定数X、Yを定義し、それらを使って計算した結果を
 * 新たな定数Zに格納しています。計算はコンパイル時に行われます。
 */

fn main() {
    const X: i32 = 123;
    const Y: i32 = 45;
    const Z: i32 = X + Y;
    println!("{} + {} = {}", X, Y, Z);
}
