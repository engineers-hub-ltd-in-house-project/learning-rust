/*
 * if文による条件分岐
 *
 * この例では、Rustでの基本的な条件分岐（if文）の使い方を示しています。
 * if文は条件が真（true）の場合に特定のコードブロックを実行します。
 * 
 * Rustのif文の特徴：
 * 1. 条件式の周りに括弧は必要ありません（ただし使用可能）
 * 2. 条件式は必ずbool型でなければなりません（暗黙的な型変換はない）
 * 3. 条件に一致する場合のコードブロックは波括弧{}で囲む必要があります
 * 
 * この例では、数値xが10より大きいかどうかをチェックし、結果に応じてメッセージを表示します。
 */

fn main() {
    let x = 15;

    if x > 10 {
        println!("xは10より大きいです");
    } else {
        println!("xは10以下です");
    }
}
