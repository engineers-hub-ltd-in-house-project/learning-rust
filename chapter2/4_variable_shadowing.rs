/*
 * 変数のシャドーイング
 *
 * この例では、Rustの変数シャドーイング（shadowing）という機能を示しています。
 * シャドーイングとは、同じ名前の変数を再度宣言することで、前の変数を「覆い隠す」技術です。
 *
 * シャドーイングの特徴：
 * 1. 新しい変数は前の変数と同じ名前を持ちますが、別の変数として扱われます
 * 2. 型を変更することができます（例：数値から文字列へ）
 * 3. スコープ内でのみ有効です
 *
 * この例では、数値として宣言した変数xを、後で文字列として再宣言（シャドーイング）しています。
 */

fn main() {
    let x = 5;
    println!("数値のx: {}", x);

    // 同じ名前で新しい変数を宣言（シャドーイング）
    let x = "文字列";
    println!("文字列のx: {}", x);
}
